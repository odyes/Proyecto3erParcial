class Menu{
    var nombre : String
    var precio1 : String
    var precio2 : String
    var tipo1 : String
    var tipo2 : String
    var foto : String
    
    init(nombre: String, precio1: String, precio2: String, tipo1: String, tipo2: String, foto: String) {
        self.nombre = nombre
        self.precio1 = precio1
        self.precio2 = precio2
        self.tipo1 = tipo1
        self.tipo2 = tipo2
        self.foto = foto
    }
    
}

